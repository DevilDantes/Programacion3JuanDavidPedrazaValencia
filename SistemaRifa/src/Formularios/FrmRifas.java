  private void cargarDatosRifa() {
    try {
        // Conexi√≥n a la base de datos
        Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/sistemarifas", 
            "root", 
            ""
        );

        String query = "SELECT id_rifa, nombre_rifa, descripcion, `fecha_inicio`, `fecha_fin`, costo_ticket FROM rifas";
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(query);

        // Limpiar la tabla antes de cargar datos nuevos
        dtm.setRowCount(0);

        while (rs.next()) {
            // Agregar datos de la base al modelo de tabla
            dtm.addRow(new Object[]{
                rs.getString("id_rifa"),
                rs.getString("nombre_rifa"),
                rs.getString("descripcion"),
                rs.getDate("fecha_inicio"),
                rs.getDate("fecha_fin"),
                rs.getDouble("costo_ticket")
            });
        }

        rs.close();
        stmt.close();
        con.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(
            this, 
            "Error al cargar datos: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE
        );
    }
}

